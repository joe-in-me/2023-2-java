package day7.ch5;

public class ArrayMission {
    public static void main(String[] args) {
        int[] arr = {100, 200, 300, 400};

        // for문 과 arr 을 이용하여 배열의 모든 방의 값을 출력하시오.
        /*
        ex)
        100
        200
        300
        400
         */

        for (int i = 0; i < arr.length; i++) {
            System.out.println(arr[i]);
        }




    }
}
